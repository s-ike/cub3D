#include "cub3d.h"

void	draw(t_info *info)
{
	int	y;
	int	x;

	y = 0;
	while (y < info->screen.h)
	{
		x = 0;
		while (x < info->screen.w)
		{
			info->img.data[y * (info->img.size_l / 4) + x] = info->buf[y][x];
			x++;
		}
		y++;
	}
	mlx_put_image_to_window(info->mlx, info->win, info->img.img, 0, 0);
}
